w=0:1:10000;
s=size(w);
m2(1,1:s(1,2))=0.300;
m1(1,1:s(1,2))=0.400;

c2(1,1:s(1,2))=0.3;
k2(1,1:s(1,2))=200000;
k2=k2+i.*c2.*sqrt(k2.*m2).*w;

c1(1,1:s(1,2))=0.01;
k1(1,1:s(1,2))=200000;
k1=k1+i.*c1.*sqrt(k1.*m1).*w;



Z=(-w.*w.*m2+k2)./((k2-m2.*w.*w).*(k1+k2-m1.*w.*w)-k2.*k2);
rho=1.2;
c=344;
t=(2*Z*i.*w*rho*c)./(1+2*Z*i.*w*rho*c);
T=20*log10(1./t);
plot(w./(2*pi()),T)